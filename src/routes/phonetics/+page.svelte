	<div class="udar">
		<h2>Ударения</h2>
		<p class="udar-p">В двусложных словах ударение падает на первый слог: <sapn class="under">a</sapn>mar, th<sapn class="under">a</sapn>la, s<sapn class="under">u</sapn>ilad.</p>
		<p class="udar-p">
			Если в слове больше двух слогов, ударение падает на второй от конца слог, если тот «долгий»
			(содержит длинную гласную, дифтонг или сопровождается удвоенным или более чем одним
			согласным): an<sapn class="under">í</sapn>rad, nelch<sapn class="under">ae</sapn>nen, erch<sapn class="under">a</sapn>mmui. (помните, что, например, «th» или «ch» - это только
			один согласный!)
		</p>
		<p class="udar-p">
			Если ничего из вышеперечисленного нет, ударение падает на третий слог от конца: n<sapn class="under">i</sapn>phredil,
			ad<sapn class="under">a</sapn>nedair.
		</p>
